public class literals_04_AG {

// A constant value which can be assigned to a variable is 
// called literal
    public static void main(String[] args)
    {
        byte age = 34;
        char ch = 'A';
        int age2 = 78;
        short age3 = 99;
        long age5 = 99998877666L;
        float f1 = 5.6f;
        double d1 = 4.66;
        boolean a = true;
        System.out.println(age);
        String str = "Harry"; 
        System.out.println(str);
  
    }
}
